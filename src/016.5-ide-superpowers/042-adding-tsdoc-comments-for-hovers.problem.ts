/**
 * Adds two numbers
 * 
 * @remarks
 * Onlu works with numbers, this is part of learning typescript
 * 
 * @param a - first number
 * @param b - second number
 * @returns The sum of first and second numbers
 * 
 * @example
 * 
 * myFunction(1, 2)
 * 
 * @beta
 */
const myFunction = (a: number, b: number) => {
  return a + b;
};
